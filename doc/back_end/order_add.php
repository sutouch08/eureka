<h1>การเพิ่มออเดอร์</h1>
<p>
เมื่อลูกค้าโทรมาซื้อสินค้า เราสามารถเปิดเอกสารสั่งสินค้าแล้วรับคำสั่งซื้อลูกค้าได้ทันที ซึ่งระบบจะแสดงยอดสินค้าคงเหลือให้เห็นเพื่อความสะดวกในการตอบคำถามลูกค้า การเพิ่มคำสั่งซื้อทำได้ดังนี้ <br />
