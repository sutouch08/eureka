<h3 style="color:red; border-bottom:solid 1px #CCC;" id="doc">การแก้ไขหัวเอกสาร</h3>
บางครั้งเราเพิ่มสินค้าเข้าไปในออเดอร์เป็นจำนวนหลายรายการแล้ว แต่เมื่อตรวจทานดูกลับพบว่า เลือกลูกค้าผิด หรือ ใส่หมายเหตุผิด หากยกเลิกออเดอร์แล้วเพิ่มใหม่คงต้องใช้เวลามาก ทางออกนี้คือการแก้ไขหัวเอกสาร ซึ่งจะเราจะสามารถแก้ไขชื่อลูกค้า วันที่ การชำระเงิน และ หมายเหตุได้ โดยที่รายการสินค้าที่เราเพิ่มไว้ในออเดอร์ยังคงอยู่เหมือนเดิม<br />
